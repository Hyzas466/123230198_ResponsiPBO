public class Datatoko {
    private int id_barang;
    private string nama_customer;
    private int harga_barang;
    private string cicilan;
    private int bunga;
    private int angsuran;
    private int total;

    public int getId_barang() {
        return id_barang;
    }

    public void setId_barang(int id_barang) {
        this.id_barang = id_barang;
    }

    public string getNama_customer() {
        return nama_customer;
    }

    public void setNama_customer(string nama_customer) {
        this.nama_customer = nama_customer;
    }

    public int getHarga_barang() {
        return harga_barang;
    }

    public void setHarga_barang(int harga_barang) {
        this.harga_barang = harga_barang;
    }

    public string getCicilan() {
        return cicilan;
    }

    public void setCicilan(string cicilan) {
        this.cicilan = cicilan;
    }

    public int getBunga() {
        return bunga;
    }

    public void setBunga(int bunga) {
        this.bunga = bunga;
    }

    public int getAngsuran() {
        return angsuran;
    }

    public void setAngsuran(int angsuran) {
        this.angsuran = angsuran;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
    
    
}
